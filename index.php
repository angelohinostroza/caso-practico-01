        Durante el primer gobierno del presidente Fernando Belaúnde Terry, un grupo de pobladores elaboraron los documentos respectivos con la finalidad de solicitar al Congreso de la República la elevación del pueblo de los Órganos a la categoría de distrito, gracias al apoyo en aquel entonces por el senador Juan Taboada Zapata y del diputado Ricardo Temoche, representante por Piura en el Congreso.
